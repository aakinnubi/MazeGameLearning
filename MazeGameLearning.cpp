int GetIndexFromCoordinates(int x, int y, int width)
{
	return x+y*width;
}

void DrawLevel(char level[], int width, int height)
{
	for (int y = 0; y < height; y++) {
		for (int x = 0; x < width; x++) {
			int index = GetIndexFromCoordinates(x, y, width);
			cout << level[index];
		}
		cout << endl;
	}
}
